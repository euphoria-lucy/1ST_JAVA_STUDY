// 1부터 10까지 숫자를 출력해보는 프로그램

package Operator;

public class Num1To10 {
	
	public static void main(String args[]){

		for (int i = 1; i<=10; i++){
		    System.out.println(i);
		}

		for (int i = 50; i<=100; i++){
		    System.out.println(i);   // for문에서는 i, j, k, l, m, n 순으로 사용하고 보통은 i, j, k만 사용한다
		}   // i를 위에서 선언하고 아래에서 선언해도 {중괄호}로 영역제한을 해서 에러안남. {중괄호}는 실행영역제한 역할을 한다.

	    }

}
